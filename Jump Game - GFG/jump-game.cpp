class Solution {
  public:
    int canReach(int arr[], int n) {
        // code here
        int mx_reach=0;
        for(int i=0;i<n;i++){
            if(mx_reach<i){
                return false;
            }
            mx_reach=max(mx_reach,arr[i]+i);
        }
        return true;
    }
};

